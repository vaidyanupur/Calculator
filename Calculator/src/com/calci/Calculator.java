package com.calci;

public class Calculator {
	
	int addition(int a, int b)
	{
		int c = a + b;
		return c;
	}
	
	int substraction(int a, int b)
	{
		int c = a - b;
		return c;
	}
	
	int multiplication(int a, int b)
	{
		int c = a * b;
		return c;
	}
	
	int division(int a, int b)
	{
		int c = a / b;
		return c;
	}
}
